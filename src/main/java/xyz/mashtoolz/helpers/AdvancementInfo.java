package xyz.mashtoolz.helpers;

public class AdvancementInfo {

	private final String key;
	private final String name;

	public AdvancementInfo(String key, String name) {
		this.key = key;
		this.name = name;
	}

	public String getKey() {
		return key;
	}

	public String getName() {
		return name;
	}
}
